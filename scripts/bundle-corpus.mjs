#!/usr/bin/env node
// Bakes content/corpus/*.md into lib/corpus.generated.ts so that runtime
// code (lib/corpus.ts, lib/provenance.ts, the three route handlers) can
// run on Cloudflare Workers' edge runtime without filesystem access.
// Run automatically by the `prebuild` hook; also safe to run manually
// after curating the corpus.

import { readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = dirname(here);
const corpusDir = join(repoRoot, "content", "corpus");
const outPath = join(repoRoot, "lib", "corpus.generated.ts");

function loadEntries() {
  let names;
  try {
    names = readdirSync(corpusDir);
  } catch {
    return [];
  }

  return names
    .filter((name) => name.endsWith(".md"))
    .sort()
    .map((filename) => {
      const abs = join(corpusDir, filename);
      if (!statSync(abs).isFile()) return null;
      const body = readFileSync(abs, "utf8");
      const trimmed = body.trim();
      const titleMatch = /^#\s+(.+?)\s*$/m.exec(body);
      if (!titleMatch) {
        throw new Error(
          `[bundle-corpus] ${filename} is missing a top-level '# Title' heading`,
        );
      }
      return {
        filename,
        title: titleMatch[1],
        body,
        trimmed,
      };
    })
    .filter(Boolean);
}

function serialize(entries) {
  const sources = entries.map((e) => ({
    filename: e.filename,
    title: e.title,
    body: e.body,
  }));
  const corpus = entries
    .map((e) => `<!-- file: ${e.filename} -->\n${e.trimmed}`)
    .join("\n\n");

  return `// AUTO-GENERATED by scripts/bundle-corpus.mjs. Do not edit by hand.
// Regenerate with \`pnpm corpus:bundle\` or the \`prebuild\` hook.

export type CorpusSource = {
  filename: string;
  title: string;
  body: string;
};

export const CORPUS_SOURCES: readonly CorpusSource[] = ${JSON.stringify(sources, null, 2)} as const;

export const YISU_CORPUS: string = ${JSON.stringify(corpus)};
`;
}

const entries = loadEntries();
if (entries.length === 0) {
  console.warn(
    "[bundle-corpus] content/corpus is empty — emitting empty bundle",
  );
}
writeFileSync(outPath, serialize(entries), "utf8");
console.log(
  `[bundle-corpus] wrote ${entries.length} source(s) to lib/corpus.generated.ts`,
);
